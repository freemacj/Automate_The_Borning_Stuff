def hello(name):
    print('Hello ' + name)

hello('Alice')
hello('Bob')

'''
In between the () is the name variable, which in this context is called a parameter

When this function is called, the name parameter is assigned the argument that it is passed ie. Alice and Bob

    Argument = The value passed in the function call. ex: ('Alice')
    Parameter = The variable inside the function. ex: (name)

'''