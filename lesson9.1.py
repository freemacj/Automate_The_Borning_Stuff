def hello(): # this uses a def statment to define our new function called hello()
    print('Howdy!') # the code in the block that follows the def statement is the body, thus whenever we call the function hello() it jumps to the top of the function
    print('Howdy!!!')
    print('Hello there.')

hello() # note: the code inside the function is only run once the function is called
hello()
